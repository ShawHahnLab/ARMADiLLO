  void chomp(string &str)
  {
    std::string whitespaces ("\n\r");
    
    std::size_t found = str.find_last_not_of(whitespaces);
    if (found!=std::string::npos)
      str.erase(found+1);
    else
      str.clear();            // str is all whitespace
  }
  

  void tokenize(const string& str, std::vector<string>& tokens, const string& delimiters = " ")
  {
    // Skip delimiters at beginning.                                                                                                                        
    string::size_type lastPos = str.find_first_not_of(delimiters, 0);
    // Find first "non-delimiter".                                                                                                                          
    //string::size_type pos     = str.find_first_of(delimiters, lastPos);                                                                                   
    size_t pos=0;
    string strtmp=str;
    std::string token;
    while ((pos = strtmp.find(delimiters)) != std::string::npos) {
      token = strtmp.substr(0, pos);
      tokens.push_back(token);
      strtmp.erase(0, pos + delimiters.length() );
    }
    tokens.push_back(strtmp);
    
  }

  
